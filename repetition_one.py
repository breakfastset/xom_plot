for i in range(3):
    print(i)
# print 0 1 2 on separate lines
print("=========")

for i in range(1, 6):
    print(i)
# print 1, 2, 3, 4, 5 on separate lines
print("=========")

for k in range(1, 20, 3):
    print(k)
# print 1, 4, 7, 10, 13, 16, 19
