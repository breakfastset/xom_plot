words_list = ["bird", "mynah", "handicapped", "toddler"]
for word in words_list:   # for each loop
    print(f"The word is {word} and has {len(word)} characters.")

# (  )    usually used for a function print() is a function
# { }    in f"{}" it is a variable
#        can also be dictionary or a set
# [ ]    is a list (a container of mutable items)


